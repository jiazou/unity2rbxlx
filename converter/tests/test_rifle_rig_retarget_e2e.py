class _Script:
    """Minimal ``_HasLuauSourceAndPath`` for driving the REAL lowering."""

    def __init__(self, source: str, source_path: str = "/proj/Player.cs") -> None:
        self.luau_source = source
        self.source_path = source_path
        self.rig_binding: dict[str, object] | None = None


def _lower(
    source: str,
    *,
    field: str = "weaponSlot",
    child: str = "WeaponSlot",
    cam_receiver: str = "cam",
    source_path: str = "/proj/Player.cs",
) -> _Script:
    """Run S1's REAL lowering over ``source``; return the edited script object."""
    s = _Script(source, source_path)
    fact = RigRootedRetargetFact(
        field_name=field, child_name=child, cam_receiver=cam_receiver
    )
    crs = ChildRefScript(
        facts=(), getchild_total=1, resolved_total=1, rig_facts=(fact,)
    )
    lower_rifle_rig_retarget([s], {source_path: crs})
    return s


def _run_pipeline_gate(
    scripts: list[RbxScript], tmp_path: Path
) -> tuple[list[str], list[dict[str, object]]]:
    """Drive the REAL ``Pipeline._run_contract_verifier`` hook (the same gate the
    corpus uses) and return ``(promoted_fail_closed_errors, recorded_rows)`` — the
    LOUD end-to-end failure surface a dropped/un-rerouted binding produces."""
    unity_project = tmp_path / "unity"
    (unity_project / "Assets").mkdir(parents=True)
    output = tmp_path / "out"
    output.mkdir()
    pipeline = Pipeline(str(unity_project), str(output))
    pipeline.state.rbx_place = RbxPlace()
    pipeline.state.rbx_place.scripts = scripts
    pipeline.ctx.scene_runtime = {}
    pipeline._run_contract_verifier({"topology": _TOPOLOGY})
    rows = pipeline.ctx.scene_runtime.get("contract_check_violations", [])
    # The fail-closed promotion is what flips conversion success — compute it the
    # same way the verifier's pure surface does (the hook records the rows; the
    # promotion set is derived by fail_closed_errors over the same checks).
    result = verify_contract(_TOPOLOGY, scripts)
    return fail_closed_errors(result), rows


