# ===========================================================================
# 1. DETERMINISTIC RACE TEST (acceptance d) — the crux.
# Awake-miss -> rig streams in LATE -> equip -> rifle on the rig's WeaponSlot.
# Runs the REAL emitted resolver under an executable ``luau`` harness.
# ===========================================================================
def _extract_resolver_method(field: str = "weaponSlot", child: str = "WeaponSlot") -> str:
    """Extract the EXACT ``function <Class>:_resolve<Child>() ... end`` the REAL
    lowering emits (not a re-typed copy), so the harness exercises shipped Luau."""
    src = _AI_OUTPUT_SHAPE
    if field != "weaponSlot":
        src = src.replace("weaponSlot", field)
    lowered = _lower(src, field=field, child=child)
    method = re.search(
        rf"function Player:_resolve{re.escape(child)}\(\).*?\n    return slot[^\n]*\nend",
        lowered.luau_source,
        re.S,
    )
    assert method is not None, "could not extract the emitted resolver method"
    return method.group(0)


# A deterministic Roblox-API stub: ``task.wait`` is the clock. The rig Model is
# inserted into ``workspace`` ON a chosen tick INSIDE the bounded retry, so the
# first ``findSlot()`` sweep MISSES (Awake-with-rig-absent) and the retry catches
# the late-streaming rig. The whole schedule is forced — no real time, no flake.
def _race_harness(resolver_method: str, *, rig_appears_on_tick: int,
                  child: str = "WeaponSlot") -> str:
    return f"""
-- ===== Deterministic Roblox-API stub (task.wait is the clock) =====
local WORKSPACE_CHILDREN = {{}}
local TICK = 0
local RIG_APPEARS_ON_TICK = {rig_appears_on_tick}

local function makeInstance(className, attrs)
    local inst
    inst = {{
        _class = className,
        _attrs = attrs or {{}},
        _children = {{}},
        Parent = nil,
        IsA = function(self, c) return self._class == c end,
        GetAttribute = function(self, k) return self._attrs[k] end,
        GetChildren = function(self) return self._children end,
        FindFirstChild = function(self, name, _recursive)
            local function walk(node)
                for _, ch in node._children do
                    if ch._name == name then return ch end
                    local found = walk(ch)
                    if found then return found end
                end
                return nil
            end
            return walk(self)
        end,
    }}
    return inst
end

-- The rig Model + its WeaponSlot child (the late-streaming target).
local weaponSlot = makeInstance("BasePart", {{}})
weaponSlot._name = "{child}"
local rig = makeInstance("Model", {{ _MainCameraRig = true }})
rig._name = "_MainCameraRig"
weaponSlot.Parent = rig
rig._children = {{ weaponSlot }}

-- workspace:GetDescendants() flattens whatever has streamed in so far.
local workspace = {{
    CurrentCamera = makeInstance("Camera", {{}}),
    GetDescendants = function(self)
        local out = {{}}
        local function collect(node)
            for _, ch in node._children do
                table.insert(out, ch)
                collect(ch)
            end
        end
        for _, top in WORKSPACE_CHILDREN do
            table.insert(out, top)
            collect(top)
        end
        return out
    end,
}}

-- task.wait is the deterministic clock: it advances TICK and, on the chosen
-- tick, STREAMS THE RIG IN (the late-arrival the bounded retry exists for).
local task = {{
    wait = function(_dt)
        TICK = TICK + 1
        if TICK == RIG_APPEARS_ON_TICK then
            table.insert(WORKSPACE_CHILDREN, rig)
        end
        return _dt
    end,
}}

-- ===== The REAL emitted resolver, verbatim from the lowering =====
local Player = {{}}
Player.__index = Player
{resolver_method}

-- ===== Drive the failing schedule =====
local self = setmetatable({{}}, Player)

-- (1) Awake runs with the rig ABSENT (no _MainCameraRig in workspace yet).
--     A non-yielding lifecycle sweep would resolve nil here.
assert(#workspace:GetDescendants() == 0, "rig must be absent at Awake")

-- (2) equip fires (GetRifle / the yielding consumer) -> the resolver's first
--     sweep MISSES, the bounded retry runs, and the rig streams in on the
--     chosen tick -> the resolver returns the rig's WeaponSlot.
local slot = self:_resolve{child}()

-- (3) assert the rifle ends up parented under the rig's slot (NOT nil, NOT
--     workspace-origin) — the read reroute seats the binding under the race.
assert(slot ~= nil, "FAIL: resolver returned nil under the late-rig schedule")
assert(slot._name == "{child}", "FAIL: resolver returned the wrong child")
assert(slot.Parent == rig, "FAIL: slot is not under the _MainCameraRig")
assert(slot.Parent:GetAttribute("_MainCameraRig") == true,
    "FAIL: parent is not the rig")

-- the rifle would be parented here (the consumer's PivotTo / .Parent = slot):
local rifle = makeInstance("Model", {{}})
rifle.Parent = slot
assert(rifle.Parent == weaponSlot, "FAIL: rifle did not seat under WeaponSlot")

-- (4) memoization: a SECOND resolve returns the SAME cached instance (the warm
--     self._<field>Cache), and a fresh instance does NOT share the cache.
local slot2 = self:_resolve{child}()
assert(slot2 == slot, "FAIL: resolver is not memoized per-instance")
local other = setmetatable({{}}, Player)
-- the rig is already streamed in for the second instance; it resolves its OWN.
local other_slot = other:_resolve{child}()
assert(other_slot == weaponSlot, "FAIL: second instance did not resolve the slot")

print("RACE_OK")
"""


def _run_luau(source: str, timeout: float = 20.0) -> subprocess.CompletedProcess:
    f = tempfile.NamedTemporaryFile(
        mode="w", suffix=".luau", delete=False, encoding="utf-8"
    )
    path = f.name
    try:
        f.write(source)
        f.close()
        return subprocess.run(
            ["luau", path], capture_output=True, text=True, timeout=timeout
        )
    finally:
        Path(path).unlink(missing_ok=True)


def _luau_available() -> bool:
    from shutil import which

    return which("luau") is not None


